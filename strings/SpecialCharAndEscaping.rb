##Ruby Foundations: Special Characters and Escaping
#
#Some characters can't just be typed inside a string and need to be entered a certain 
#way. These are called special characters, and the process by which they are put in 
#to a string is called escaping.

puts "Hello\nWorld" #new line

puts "Hello\tworld" #tab

puts "Hello\bworld" #backspace

puts "Hello\vworld" #vertical tab

##If we want to use strings with " " or ' '

puts "Hello\"world\""

puts "Hello\'world\'"

#How do you type a backslash?

puts "Hello\\World"

